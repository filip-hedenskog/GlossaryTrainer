namespace GlossaryTrainer.Models;

public static class Tooltip
{
    public static string Iremasu => """
        Explanation

        いれます（入れます） – iremasu

        Meaning: to put something into something, to insert, or to add.

        It is an ichidan verb (る-verb).

        Typical usages:

        - Put an object into something
        - Add an ingredient
        - Insert something (like data, a coin, etc.)

        Examples:

        コーヒーに砂糖を入れます。
        Kōhī ni satō o iremasu.
        “I add sugar to coffee.”
        かばんに本を入れました。
        “I put a book in the bag.”

        Grammar note:

        The thing being inserted takes を (o).
        The container often takes に (ni).

        Pattern:

        [container] に [object] を 入れます
        """;
}
