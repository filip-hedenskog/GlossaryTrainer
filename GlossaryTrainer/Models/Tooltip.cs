    public static string Itai => """
    Explanation

    痛い (いたい) – “painful” or “it hurts”
    This is an i-adjective, so it can describe a state directly:

    Usage examples:

    頭が痛い。
    “My head hurts.”
    足が痛いです。
    “My leg hurts.” (polite)

    💡 Nuance:

    You can combine it with です for politeness: 痛いです.
    If responding to someone asking どうしましたか？, just saying 痛い is perfectly natural.
    Can also be used metaphorically for emotional pain:
    心が痛い。 – “My heart hurts / I feel emotional pain.”
    """;

    public static string Atama => """
    Explanation

    頭 (あたま) – literally head
    Can refer to:
    The physical head of a person or animal
    Figuratively, as in “brains” or “mind” (e.g., 頭がいい – “smart / clever”)

    Usage examples:

    頭が痛い。
    “My head hurts.” (links perfectly with 痛い)
    頭を使う。
    “Use your brain / think hard.”

    💡 Grammar note:

    This is a noun. To describe something about the head, you usually combine it with particles like が, を, or の.
    """;

    public static string Kubi => """
    Explanation

    首 (くび) – “neck”
    Refers to the body part connecting head and shoulders.
    Can also be used metaphorically in expressions like:
    首を長くして待つ – “to wait eagerly” (literally: “wait with a long neck”)
    首になる – “to get fired” (literally: “to lose one’s neck”)

    Usage examples:

    首が痛い。
    “My neck hurts.”
    首を回してください。
    “Please rotate your neck.”

    💡 Grammar note:

    Like 頭, it’s a noun. To talk about pain, combine it with が + 痛い:
    [body part] が痛い
    """;

    public static string Denki => """
    Explanation

    でんき (denki) – electricity, electric, or light (like the lights in a room)
    Commonly used for:
    Electric power: でんきをつける – “turn on the light / electricity”
    Electricity itself: でんきがきれた – “the power went out”

    Usage examples (minimal kanji):

    でんきをつけてください。
    “Please turn on the light.”
    でんきがない。
    “There is no electricity / the power is out.”

    💡 Grammar note:

    Usually a noun, but can combine with verbs like つける (turn on) or けす (turn off) when talking about lights.
    """;

    public static string SNS => """
    Explanation

    えすえぬえす (SNS) – stands for social networking service, basically “social media”
    Typically written in katakana because it’s a loanword / abbreviation from English
    Used in casual and formal contexts:
    SNSを見ます – “I look at social media.”
    SNSで友だちと話します – “I chat with friends on social media.”

    💡 Grammar note:

    SNS is a noun, so combine with particles like を (object), で (location/tool), の (possessive) as usual.
    Often written in all caps (SNS) even in Japanese text.
    """;

    public static string Hotondo => """
    Explanation

    ほとんど (hotondo) – “almost,” “mostly,” or “nearly all”
    Can be used with affirmative or negative verbs, but meaning slightly changes:

    Usage examples:

    Affirmative verb:
    ほとんどの人が来ました。
    “Almost everyone came.”
    Negative verb:
    ほとんどわかりません。
    “I hardly understand / I almost don’t understand.”

    💡 Grammar notes:

    Often used with の when modifying nouns: ほとんどの[thing]
    Can also be used adverbially before verbs to indicate “almost”
    """;

    public static string Ude => """
