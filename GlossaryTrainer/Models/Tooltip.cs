    public static string Korobimasu => """
    Explanation
    ころびます (転びます) – to fall down or to trip
    polite -ます form, so casual dictionary form is ころぶ

    Examples:

    つまずいてころびました – “i tripped and fell”
    ゆかでころばないようにきをつけて – “be careful not to fall on the floor”

    💡 Notes:

    Use で to indicate location of the fall: ゆかでころぶ
    Can also combine with て-form to describe a sequence: ころんでけがをした – “i fell and got hurt”
    """;

    public static string Uchimasu => """
    Explanation
    うちます (打ちます) – to hit, strike, or punch something
    polite -ます form, dictionary form is うつ

    Examples:

    ボールをうちます – “i hit the ball”
    かばんをうってしまいました – “i accidentally hit the bag”
    in the sentence あたまをうちました, it means “i hit / bumped my head”
    """;

    public static string Kizu => """
    Explanation (minimal kanji)

    きず (傷) – a wound, cut, or injury on the body
    can be small (like a scratch) or more serious

    Examples:

    あたまにきずがあります – “i have a cut on my head”
    くびにきずをつくりました – “i made a cut on my neck”

    💡 Notes:

    Often used with body parts to describe injuries
    Can also be used metaphorically for emotional or abstract “wounds,” but most often physical
    """;

    public static string Yakkyoku => """
    Explanation (minimal kanji)

    やっきょく (薬局) – a place where you get medicine / a pharmacy
    often used when talking about buying medicine or seeing a pharmacist

    Examples:

    やっきょくにいきます – “i am going to the pharmacy”
    かぜのくすりをやっきょくでかいました – “i bought cold medicine at the pharmacy”

    💡 Notes:

    Can combine with に for location: やっきょくに – “to the pharmacy”
    Can also use で to indicate where you do something: やっきょくでかう – “buy at the pharmacy”
    """;

    public static string Shibaraku => """
    Explanation (minimal kanji)

    しばらく – for a short time, for a while, or temporarily
    often used to describe a period that is not exact

    Examples:

    しばらくまってください – “please wait for a while”
    しばらくいえをあけます – “i will be away from home for a while”

    💡 Notes:

    Can be used at the start of a conversation to mean “long time no see”: しばらくですね – “it’s been a while”
    Usually does not combine with exact numbers; it is vague in duration
    """;

    public static string Naorimasu => """
    Explanation (minimal kanji)

    なおります (治ります) – to get better, to recover from illness or injury, or for something broken to be fixed
    polite -ます form, dictionary form is なおる

    Examples:

    かぜがなおります – “my cold will get better”
    きずがなおります – “the wound will heal”
    でんきがなおります – “the electricity / device will be fixed”

    💡 Notes:

    Use が to indicate what is recovering or being fixed
    Can be used for both health and things that are broken
    Often appears in polite or neutral contexts when talking about recovery
    """;

    public static string Nurimasu => """
    Explanation (minimal kanji)

    ぬります (塗ります) – to paint, to apply something (like cream, ointment, or paint), or to spread over a surface
    polite -ます form, dictionary form is ぬる

    Examples:

    くすりをきずにぬります – “i apply medicine to the wound”
    へやのかべをぬります – “i paint the wall”
    はをぬります – “i apply something to my teeth” (like toothpaste or treatment)

    💡 Notes:

    Can combine with に to indicate the target: きずにぬる – “apply to the wound”
    Often used in medical, cosmetic, or painting contexts
